/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  int cnt=0;
  int temp=0;
  int k= (0x01<<24)+(0x01<<16)+(0x01<<8)+0x01; // make test int k=0x01010101;
  temp += k&x;
  temp += k&(x>>1);
  temp += k&(x>>2);
  temp += k&(x>>3);
  temp += k&(x>>4);
  temp += k&(x>>5);
  temp += k&(x>>6);
  temp += k&(x>>7);
  cnt+=temp&0xff;
  cnt+=(temp>>8)&0xff;
  cnt+=(temp>>16)&0xff;
  cnt+=(temp>>24)&0xff;
  return cnt;
}
