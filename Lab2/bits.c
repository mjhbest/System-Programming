/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
  int sign = x&0x80000000;
  int exp=0;
  int temp = 1<<31;
  int i=32;
  int frag=0;
  int result;
  int check1,check2,check3,del; //for rounding

  if(sign & 0x80000000) x=-x; //if it's 

  while(i>0){

    if(temp & x) {
      exp=((i-1)+127)<<23;
      break;
    }
    i--;
    temp = temp>>1;
  } //same as log(searching bit most left)
  del=i-1;
  frag = x&(0xffffffff+(1<<del));
  if(del <=23){ // no rounding required
    frag = frag << (24-i);
    result = sign + exp + frag;
  }
  else{ //check the round or not
    del = i-24;
    temp = 1<<(del-1);
    check1 = (frag>>(del-1))&1; //round needed? check most left bit in deleted bits
    check2= frag &(temp-1); //is there any bits which will be deleted
    check3 = (frag>>del)&1; // it's even?
    frag = frag >> del;
    result = sign + exp + frag;
    result += check1 &&(check2||check3); // (even or over) and (round needed)
  }
  return result;
}
